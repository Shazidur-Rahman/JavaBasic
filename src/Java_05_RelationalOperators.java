public class Java_05_RelationalOperators {
	public static void main(String[] args) {
		boolean result;

		result = (10 == 10);
		System.out.println("The Result is: "+result);

		result = (5 < 5);
		System.out.println("The Result is: "+result);

		result = (5 <= 10);
		System.out.println("The Result is: "+result);

		result = (10 > 5);
		System.out.println("The Result is: "+result);

		result = (10 >= 5);
		System.out.println("The Result is: "+result);

		result = (2 != 2);
		System.out.println("The Result is: "+result);

	}
}
