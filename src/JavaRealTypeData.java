public class JavaRealTypeData {
	public static void main(String[] args) {
		float myFloat;

		myFloat = 50.33F;
		System.out.println(" the value of myFloat = "+myFloat);

		myFloat = (float) 70/3;
		System.out.println(" the value of myFloat = "+myFloat);

		double myDouble;
		myDouble = -0.3432D;
		System.out.println(" the value of myDouble = "+myDouble);

		myDouble = 12D/7;
		System.out.println(" the value of myDouble = "+myDouble);
	}
}
