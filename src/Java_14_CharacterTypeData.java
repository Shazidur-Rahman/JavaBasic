public class Java_14_CharacterTypeData {
	public static void main(String[] args) {
		char myChar;

		myChar = 'A';
		System.out.println("The value of myChar: "+myChar);

		myChar = 66; //ASCII value
		System.out.println("The value of myChar: "+myChar);

		myChar = '\u0043'; //unicode hexadecimal value
		System.out.println("The value of myChar: "+myChar);

	}
}
